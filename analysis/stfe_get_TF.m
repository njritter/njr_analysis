    TF.mmm_avg, ...
    TF.mmm_ste, ...
    TF.dir2_all, ...
    TF.dir2_avg, ...
    TF.dir2_ste] = stfe_get_alldir(stiminfo,pvalue);

[TF.bs_mmm_all, ...
    TF.bs_mmm_avg, ...
    TF.bs_mmm_ste, ...
    TF.bs_dir2_all, ...
    TF.bs_dir2_avg, ...
    TF.bs_dir2_ste] = stfe_bs_get_alldir(stiminfo,pvalue);
