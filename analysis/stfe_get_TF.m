function [TF] = stfe_get_TF(stiminfo,pvalue)

%
%
%
%
%
%
%
%
%
%


[TF.pref_all, ...
    TF.pref_avg, ...
    TF.pref_ste, ...
    TF.null_all, ...
    TF.null_avg, ...
    TF.null_ste] = stfe_get_prefnull(stiminfo,pvalue);

